        public static float DrawText(OpenTK.GameWindow wnd, string text, float posX, float posY, float scale, Color color, bool center = false)
        {
            posX /= (float)wnd.Width;
            posY /= (float)wnd.Height;

            scale *= 0.05f;

            float ratio = wnd.Width / (float)wnd.Height;
            float scaleX = scale;
            float scaleY = scale * ratio;

            if (center)
            {
                float w = 0;
                foreach (char c in text)
                {
                    w += GetCharWidth((int)c) * scaleX;
                }

                posX -= w * 0.5f;
            }

            GL.Begin(PrimitiveType.Lines);
            GL.Color4(color);
            foreach (char c in text)
            {
                posX += DrawChar((int)c, posX, posY, scaleX, scaleY);
            }
            GL.End();

            return posX;
        }


